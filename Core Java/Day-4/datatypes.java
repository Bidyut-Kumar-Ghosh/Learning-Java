public class datatypes {
    public static void main(String[] args) 
    {
        //Primitive Data type 

        int myRollNum = 10; // int stores 4-bytes
        float myFloatNum = 7.99f; // float stores 4-bytes
        char myLetter ='D'; //char stores 2-bytes
        boolean myBool = true; //boolean stores 1-bytes

        //Non-Primitive Data Types

        String myNameText = "Bidyut"; 


        //Output
        System.out.println(myRollNum);
        System.out.println(myFloatNum);
        System.out.println(myLetter);
        System.out.println(myBool);
        System.out.println(myNameText);

    }
}
